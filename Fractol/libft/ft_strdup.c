#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	size_t	size;
	size_t	i;

	size = ft_strlen(src);
	dest = (char *)malloc(sizeof(*src) * (size + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
